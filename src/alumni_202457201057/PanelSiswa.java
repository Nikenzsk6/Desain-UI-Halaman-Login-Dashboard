    private void b_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_ubahActionPerformed
        // TODO add your handling code here:
        int baris = table_DataSiswa.getSelectedRow();//ambil baris
        
        if (baris>=0){
        String nis = t_NIS.getText().trim();
        String nama = t_Nama.getText().trim();
        String gender = c_JenisKelamin.getSelectedItem().toString();
        String tmptlahir = t_TempatLahir.getText().trim();
        String tgllahir = t_TanggalLahir.getText().trim();
        String no_hp = t_NoHp.getText().trim();
        String kelas = c_Kelas.getSelectedItem().toString();
        String alamat = t_Alamat.getText().trim();
            
            if ((!nis.isEmpty()&&!nama.isEmpty() && !gender.isEmpty()&& !tmptlahir.isEmpty()) 
           && (!tgllahir.isEmpty() && !no_hp.isEmpty() && !kelas.isEmpty() && !alamat.isEmpty() && !pathFoto.isEmpty())) {
           
             // Ambil model dari tabel
        DefaultTableModel model = (DefaultTableModel) table_DataSiswa.getModel();

        // Update data di baris yang dipilih
        model.setValueAt(nis, baris, 0);
        model.setValueAt(nama, baris, 1);
        model.setValueAt(gender, baris, 2);
        model.setValueAt(tmptlahir, baris, 3);
        model.setValueAt(tgllahir, baris, 4);
        model.setValueAt(no_hp, baris, 5);
        model.setValueAt(kelas, baris, 6);
        model.setValueAt(alamat, baris, 7);
        model.setValueAt(pathFoto, baris, 8);
            
            //kosongkan textField
            t_NIS.setText("");
            t_Nama.setText("");
            c_JenisKelamin.setSelectedIndex(0);
            t_TempatLahir.setText("");
            t_TanggalLahir.setText("");
            t_NoHp.setText("");
            c_Kelas.setSelectedIndex(0);
            t_Alamat.setText("");
            t_Foto.setIcon(null);
            t_Foto.setText("Foto");
            pathFoto = null;
            
            JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
        } else {
            JOptionPane.showMessageDialog(null, "Lengkapi data terlebih dahulu");
        }
        }else {
             JOptionPane.showMessageDialog(null, "Pilih data yang ingin diubah");
          }
    }//GEN-LAST:event_b_ubahActionPerformed

    private void b_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_hapusActionPerformed
        // TODO add your handling code here:
        int baris = table_DataSiswa.getSelectedRow(); // ambil baris yang dipilih

        if (baris >= 0) {
        int konfirmasi = JOptionPane.showConfirmDialog(
            null,
            "Apakah kamu yakin ingin menghapus data ini?",
            "Konfirmasi Hapus",
            JOptionPane.YES_NO_OPTION
        );

        if (konfirmasi == JOptionPane.YES_OPTION) {
            DefaultTableModel model = (DefaultTableModel) table_DataSiswa.getModel();
            model.removeRow(baris); // hapus baris dari model

            // Kosongkan TextField setelah hapus
             t_NIS.setText("");
            t_Nama.setText("");
            c_JenisKelamin.setSelectedIndex(0);
            t_TempatLahir.setText("");
            t_TanggalLahir.setText("");
            t_NoHp.setText("");
            c_Kelas.setSelectedIndex(0);
            t_Alamat.setText(" ");
            t_Foto.setIcon(null);
            t_Foto.setText("Foto");
            pathFoto = null;
        }

        } else {
        JOptionPane.showMessageDialog(null, "Pilih data yang ingin dihapus!");
        }
    }//GEN-LAST:event_b_hapusActionPerformed

