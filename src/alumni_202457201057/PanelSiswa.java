    private void b_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_tambahActionPerformed
        // TODO add your handling code here:
        String nis = t_NIS.getText().trim();
        String nama = t_Nama.getText().trim();
        String gender = c_JenisKelamin.getSelectedItem().toString();
        String tmptlahir = t_TempatLahir.getText().trim();
        String tgllahir = t_TanggalLahir.getText().trim();
        String no_hp = t_NoHp.getText().trim();
        String kelas = c_Kelas.getSelectedItem().toString();
        String alamat = t_Alamat.getText().trim();
       
        if ((!nis.isEmpty()&&!nama.isEmpty() && !gender.isEmpty()&& !tmptlahir.isEmpty()) 
           && (!tgllahir.isEmpty() && !no_hp.isEmpty() && !kelas.isEmpty() && !alamat.isEmpty())){
           DefaultTableModel model= (DefaultTableModel) table_DataSiswa.getModel();
           model.addRow(new Object[]{nis,nama,gender,tmptlahir,tgllahir,no_hp,kelas,alamat,pathFoto});
           
           //Kosongkan textField setelah tambah
        t_NIS.setText("");
        t_Nama.setText("");
        c_JenisKelamin.setSelectedIndex(0);
        t_TempatLahir.setText("");
        t_TanggalLahir.setText("");
        t_NoHp.setText("");
        c_Kelas.setSelectedIndex(0);
        t_Alamat.setText("");
        t_Foto.setIcon(null);
        t_Foto.setText("Foto");
        pathFoto = null;
         
        } else {
            JOptionPane.showMessageDialog(null, "Lengkapi semua data terlebih dahulu!");
        }
    }//GEN-LAST:event_b_tambahActionPerformed

